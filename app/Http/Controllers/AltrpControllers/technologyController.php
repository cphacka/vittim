     * Store a newly created resource in storage.
     *
     * @param Request $request
     *
     * @return \Illuminate\Http\JsonResponse
     */
    public function store(technologyStoreRequest $request)
    {
        $data = $request->all();
        if ($fileInfo = $this->hasFileInRequest($request)) {
            $medias = $this->saveMedias($request, $fileInfo['relation']);
            foreach ($medias as $media) {
                $data[$fileInfo['foreign_key']] = $media->id;
            }
        }

        $technology = new technology;

        $regChat = ChatService::registerInChat($technology->getTable(), $data);

        $data = $this->hashPasswordAttributeIfExists($technology->getTable(), $data);

        $technology->fill($data);

        $res = $technology->save();
        if($res){
            return response()->json(['success'=>true, 'data'=>$technology], 200, [], JSON_UNESCAPED_UNICODE);
        }

        return response()->json(['success'=>false, 'message' => trans("responses.dberror")], 400, [], JSON_UNESCAPED_UNICODE);
    }

    /**
     * Display the specified resource.
     *
     * @param int $id
     *
     * @return \Illuminate\Http\JsonResponse
     */
    public function show($id)
    {
        $technology = technology::find($id);

        if(! $technology) {
            return response()->json(trans("responses.not_found.technology"), 404, [], JSON_UNESCAPED_UNICODE);
        }

        $model = Model::where('name', 'technology')->first();
        $relations = Relationship::where([['model_id',$model->id],['always_with',1]])->get()->implode('name',',');
        $relations = $relations ? explode(',',$relations) : false;

        if ($relations) {
            $technology = $technology->load($relations);
        }

        $technology = $this->getRemoteData($model, $technology, true);

        return response()->json($technology, 200, [], JSON_UNESCAPED_UNICODE);
    }

    /**
     * Update the specified resource in storage.
     *
     * @param \Illuminate\Http\Request $request
     * @param int $id
     *
     * @return \Illuminate\Http\JsonResponse
     */
    public function update(technologyUpdateRequest $request, $id)
    {
        $data = $request->all();
        if ($fileInfo = $this->hasFileInRequest($request)) {
            $medias = $this->saveMedias($request, $fileInfo['relation']);
            foreach ($medias as $media) {
                $data[$fileInfo['foreign_key']] = $media->id;
            }
        }

        $technology = technology::find($id);

        if(! $technology) {
            return response()->json(['success'=>false, 'message' => trans("responses.not_found.technology")], 404, [], JSON_UNESCAPED_UNICODE);
        }

        $data = $this->hashPasswordAttributeIfExists($technology->getTable(), $data);

        $result = $technology->update($data);

        if($result){
            return response()->json(['success'=>true], 200, [], JSON_UNESCAPED_UNICODE);
        }

        return response()->json(['success'=>false, 'message' => trans("responses.dberror")], 400, [], JSON_UNESCAPED_UNICODE);
    }

    /**
     * Remove the specified resource from storage.
     *
     * @param int $id
     *
     * @return \Illuminate\Http\JsonResponse
     */
    public function destroy($id)
    {
        $technology = technology::find($id);

        if(! $technology) {
            return response()->json(['success'=>false, 'message' => trans("responses.not_found.technology")], 404, [], JSON_UNESCAPED_UNICODE);
        }

        $result = $technology->delete();

        if($result) {
            return response()->json(['success'=>true, 'message' => trans("responses.delete.technology")], 200, [], JSON_UNESCAPED_UNICODE);
        }

        return response()->json(['success'=>false, 'message' => trans("deleteerror")], 400, [], JSON_UNESCAPED_UNICODE);
    }

    /**
     * Update column of resource from storage.
     *
     * @param ApiRequest $request
     * @param $id
     * @param $column
     * @return \Illuminate\Http\JsonResponse
     */
    public function updateColumn(ApiRequest $request, $id, $column)
    {
        $technology = technology::find($id);

        if(! $technology) {
            return response()->json(['success'=>false, 'message' =>"technology not found"], 404, [], JSON_UNESCAPED_UNICODE);
        }

        if (! isset($technology->$column)) {
            return response()->json(['success'=>false, 'message'=>'Column not exists'], 400, [], JSON_UNESCAPED_UNICODE);
        }

        $technology->$column = $request->column_value;

        if ($technology->save()) {
            return response()->json(['success'=>true], 200, [], JSON_UNESCAPED_UNICODE);
        }

        return response()->json('Failed to update', 400, [], JSON_UNESCAPED_UNICODE);
    }

    public function getIndexedColumnsValueWithCount($column)
    {
        $technologies = technology::selectRaw("$column as value, COUNT($column) as count")
                            ->havingRaw("COUNT($column)")
                            ->groupBy("$column")
                            ->get();
        return response()->json($technologies, 200, [], JSON_UNESCAPED_UNICODE);
    }

