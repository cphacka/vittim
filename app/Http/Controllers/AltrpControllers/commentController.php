    /**
     * commentController constructor.
     */
    public function __construct()
    {
        $this->modelClass = comment::class;
    }

    /**
     * Store a newly created resource in storage.
     *
     * @param Request $request
     *
     * @return \Illuminate\Http\JsonResponse
     */
    public function store(commentStoreRequest $request)
    {
        $data = $request->all();
        if ($fileInfo = $this->hasFileInRequest($request)) {
            $medias = $this->saveMedias($request, $fileInfo['relation']);
            foreach ($medias as $media) {
                $data[$fileInfo['foreign_key']] = $media->id;
            }
        }

        $comment = new comment;

        $regChat = ChatService::registerInChat($comment->getTable(), $data);

        $data = $this->hashPasswordAttributeIfExists($comment->getTable(), $data);

        $comment->fill($data);

        $res = $comment->save();
        if($res){
            return response()->json(['success'=>true, 'data'=>$comment], 200, [], JSON_UNESCAPED_UNICODE);
        }

        return response()->json(['success'=>false, 'message' => trans("responses.dberror")], 400, [], JSON_UNESCAPED_UNICODE);
    }

    /**
     * Display the specified resource.
     *
     * @param int $id
     *
     * @return \Illuminate\Http\JsonResponse
     */
    public function show($id)
    {
        $comment = comment::find($id);

        if(! $comment) {
            return response()->json(trans("responses.not_found.comment"), 404, [], JSON_UNESCAPED_UNICODE);
        }

        $model = Model::where('name', 'comment')->first();
        $relations = Relationship::where([['model_id',$model->id],['always_with',1]])->get()->implode('name',',');
        $relations = $relations ? explode(',',$relations) : false;

        if ($relations) {
            $comment = $comment->load($relations);
        }

        $comment = $this->getRemoteData($model, $comment, true);

        return response()->json($comment, 200, [], JSON_UNESCAPED_UNICODE);
    }

    /**
     * Update the specified resource in storage.
     *
     * @param \Illuminate\Http\Request $request
     * @param int $id
     *
     * @return \Illuminate\Http\JsonResponse
     */
    public function update(commentUpdateRequest $request, $id)
    {
        $data = $request->all();
        if ($fileInfo = $this->hasFileInRequest($request)) {
            $medias = $this->saveMedias($request, $fileInfo['relation']);
            foreach ($medias as $media) {
                $data[$fileInfo['foreign_key']] = $media->id;
            }
        }

        $comment = comment::find($id);

        if(! $comment) {
            return response()->json(['success'=>false, 'message' => trans("responses.not_found.comment")], 404, [], JSON_UNESCAPED_UNICODE);
        }

        $data = $this->hashPasswordAttributeIfExists($comment->getTable(), $data);

        $result = $comment->update($data);

        if($result){
            return response()->json(['success'=>true], 200, [], JSON_UNESCAPED_UNICODE);
        }

        return response()->json(['success'=>false, 'message' => trans("responses.dberror")], 400, [], JSON_UNESCAPED_UNICODE);
    }

    /**
     * Remove the specified resource from storage.
     *
     * @param int $id
     *
     * @return \Illuminate\Http\JsonResponse
     */
    public function destroy($id)
    {
        $comment = comment::find($id);

        if(! $comment) {
            return response()->json(['success'=>false, 'message' => trans("responses.not_found.comment")], 404, [], JSON_UNESCAPED_UNICODE);
        }

        $result = $comment->delete();

        if($result) {
            return response()->json(['success'=>true, 'message' => trans("responses.delete.comment")], 200, [], JSON_UNESCAPED_UNICODE);
        }

        return response()->json(['success'=>false, 'message' => trans("deleteerror")], 400, [], JSON_UNESCAPED_UNICODE);
    }

    /**
     * Update column of resource from storage.
     *
     * @param ApiRequest $request
     * @param $id
     * @param $column
     * @return \Illuminate\Http\JsonResponse
     */
    public function updateColumn(ApiRequest $request, $id, $column)
    {
        $comment = comment::find($id);

        if(! $comment) {
            return response()->json(['success'=>false, 'message' =>"comment not found"], 404, [], JSON_UNESCAPED_UNICODE);
        }

        if (! isset($comment->$column)) {
            return response()->json(['success'=>false, 'message'=>'Column not exists'], 400, [], JSON_UNESCAPED_UNICODE);
        }

        $comment->$column = $request->column_value;

        if ($comment->save()) {
            return response()->json(['success'=>true], 200, [], JSON_UNESCAPED_UNICODE);
        }

        return response()->json('Failed to update', 400, [], JSON_UNESCAPED_UNICODE);
    }

    public function getIndexedColumnsValueWithCount($column)
    {
        $comments = comment::selectRaw("$column as value, COUNT($column) as count")
                            ->havingRaw("COUNT($column)")
                            ->groupBy("$column")
                            ->get();
        return response()->json($comments, 200, [], JSON_UNESCAPED_UNICODE);
    }

