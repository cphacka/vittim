     * Display the specified resource.
     *
     * @param int $id
     *
     * @return \Illuminate\Http\JsonResponse
     */
    public function show($id)
    {
        $org_team = org_team::find($id);

        if(! $org_team) {
            return response()->json(trans("responses.not_found.org_team"), 404, [], JSON_UNESCAPED_UNICODE);
        }

        $model = Model::where('name', 'org_team')->first();
        $relations = Relationship::where([['model_id',$model->id],['always_with',1]])->get()->implode('name',',');
        $relations = $relations ? explode(',',$relations) : false;

        if ($relations) {
            $org_team = $org_team->load($relations);
        }

        $org_team = $this->getRemoteData($model, $org_team, true);

        return response()->json($org_team, 200, [], JSON_UNESCAPED_UNICODE);
    }

