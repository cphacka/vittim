     *
     * @return \Illuminate\Http\JsonResponse
     */
    public function store(startup_newsStoreRequest $request)
    {
        $data = $request->all();
        if ($fileInfo = $this->hasFileInRequest($request)) {
            $medias = $this->saveMedias($request, $fileInfo['relation']);
            foreach ($medias as $media) {
                $data[$fileInfo['foreign_key']] = $media->id;
            }
        }

        $startup_news = new startup_news;

        $regChat = ChatService::registerInChat($startup_news->getTable(), $data);

        $data = $this->hashPasswordAttributeIfExists($startup_news->getTable(), $data);

        $startup_news->fill($data);

        $res = $startup_news->save();
        if($res){
            return response()->json(['success'=>true, 'data'=>$startup_news], 200, [], JSON_UNESCAPED_UNICODE);
        }

        return response()->json(['success'=>false, 'message' => trans("responses.dberror")], 400, [], JSON_UNESCAPED_UNICODE);
    }

    /**
     * Display the specified resource.
     *
     * @param int $id
     *
     * @return \Illuminate\Http\JsonResponse
     */
    public function show($id)
    {
        $startup_news = startup_news::find($id);

        if(! $startup_news) {
            return response()->json(trans("responses.not_found.startup_news"), 404, [], JSON_UNESCAPED_UNICODE);
        }

        $model = Model::where('name', 'startup_news')->first();
        $relations = Relationship::where([['model_id',$model->id],['always_with',1]])->get()->implode('name',',');
        $relations = $relations ? explode(',',$relations) : false;

        if ($relations) {
            $startup_news = $startup_news->load($relations);
        }

        $startup_news = $this->getRemoteData($model, $startup_news, true);

        return response()->json($startup_news, 200, [], JSON_UNESCAPED_UNICODE);
    }

