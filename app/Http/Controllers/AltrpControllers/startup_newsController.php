     * Remove the specified resource from storage.
     *
     * @param int $id
     *
     * @return \Illuminate\Http\JsonResponse
     */
    public function destroy($id)
    {
        $startup_news = startup_news::find($id);

        if(! $startup_news) {
            return response()->json(['success'=>false, 'message' => trans("responses.not_found.startup_news")], 404, [], JSON_UNESCAPED_UNICODE);
        }

        $result = $startup_news->delete();

